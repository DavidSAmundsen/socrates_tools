SUBROUTINE SL296

  IMPLICIT REAL*8 (V)

  DIMENSION c_s296(2003)
  COMMON /SH2O/ V1S,V2S,DVS,NPTS,S(2003)                            

  OPEN(UNIT=iu_s296, FILE='mt_ckd_v3.0_s296')
  CALL write_head(iu_s296, V1S, V2S, DVS, NPTS)

  DO J = 1, NPTS                                           
    VJ = V1S+DVS* REAL(J-1)
    c_s296(J) = S(J)*sfac(VJ)
  END DO

  WRITE(iu_s296, '(4(3X, 1ES12.5))') c_s296
  CALL write_tail(iu_s296)
  CLOSE(iu_s296)
                                               
END SUBROUTINE SL296


